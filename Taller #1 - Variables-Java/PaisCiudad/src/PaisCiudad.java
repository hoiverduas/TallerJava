public class PaisCiudad {

    private String ciudad;

    private String pais;

    public PaisCiudad(String ciudad, String pais) {
        this.ciudad = ciudad;
        this.pais = pais;
    }

    public String getCiudad() {
        return ciudad;
    }

    public String getPais() {
        return pais;
    }
}
